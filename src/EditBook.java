     public void populateArrayList() {
        try {
            FileInputStream file = new FileInputStream("Books.dat");
            ObjectInputStream inputFile = new ObjectInputStream(file);

            boolean endOfFile = false;
            while (!endOfFile) {
                try {
                    books2.add((Book) inputFile.readObject());
                } catch (EOFException e) {
                    endOfFile = true;
                } catch (Exception f) {
                    JOptionPane.showMessageDialog(null,
                            f.getMessage());
                }
            }
            inputFile.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null,
                    e.getMessage());
        }
     }
    public void populateTable(ArrayList<Book> bksArray) {
        dtm2.setRowCount(0);
        for (int i = 0; i < bksArray.size(); i++) {
            Object[] obj = {bksArray.get(i).getBookName(), bksArray.get(i).getWriter(), bksArray.get(i).getPage()};
            dtm2.addRow(obj);
        }
    }

