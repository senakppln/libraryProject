    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String searchText = this.tfSearch.getText().trim();
        if (!searchText.isEmpty()) {
            searchedBooks2.clear();
            for (int j = 0; j < books2.size(); j++) {
                if (books2.get(j).getBookName().contains(searchText)) {
                    searchedBooks2.add(books2.get(j));
                }
            }
            populateTable(searchedBooks2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void Table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel  tblModel= (DefaultTableModel)Table1.getModel();
        String tblName= tblModel.getValueAt(Table1.getSelectedRow(), 0).toString();
        String tblWriter= tblModel.getValueAt(Table1.getSelectedRow(), 1).toString();
        String tblPage= tblModel.getValueAt(Table1.getSelectedRow(), 2).toString();
        
        tfName.setText(tblName);
        tfWriter.setText(tblWriter);
        tfPage.setText(tblPage);
    }//GEN-LAST:event_Table1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel  tblModel= (DefaultTableModel)Table1.getModel();
        if(Table1.getSelectedRowCount()==1){
            String name= tfName.getText();
            String writer= tfWriter.getText();
            String page=tfPage.getText();
            
            
            tblModel.setValueAt(name, Table1.getSelectedRow(), 0);
            tblModel.setValueAt(writer, Table1.getSelectedRow(), 1);
            tblModel.setValueAt(page, Table1.getSelectedRow(), 2);
            
            JOptionPane.showMessageDialog(this,"update successfully");
        }else{
            if(Table1.getRowCount()==0){
             JOptionPane.showMessageDialog(this,"Table is empty");
               
            }else{
             JOptionPane.showMessageDialog(this,"Please select a row for upgrate");

            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

